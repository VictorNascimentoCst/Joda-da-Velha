class jogo {
    constructor() {
        this.regulador = x
    }
}

function show(slot) {
    var id = slot.id
    var y = document.getElementById(id)
    if(this.regulador == 'x') {
        y.setAttribute("class", "slot simboloX") 
        this.regulador = "o"
    } else {
        y.setAttribute("class", "slot simboloO") 
        this.regulador = "x"
    }
   
}